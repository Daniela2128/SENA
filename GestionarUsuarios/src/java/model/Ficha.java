public class Ficha {
      int id;
      int codficha;
      int cantapre;
      int codprod;
      int codaprendiz;

    public Ficha() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getCodficha() {
        return codficha;
    }

    public void setCodficha(int codficha) {
        this.codficha = codficha;
    }

    public int getCantapre() {
        return cantapre;
    }

    public void setCantapre(int cantapre) {
        this.cantapre = cantapre;
    }

    public int getCodprod() {
        return codprod;
    }

    public void setCodprod(int codprod) {
        this.codprod = codprod;
    }

    public int getCodaprendiz() {
        return codaprendiz;
    }

    public void setCodaprendiz(int codaprendiz) {
        this.codaprendiz = codaprendiz;
    }
      
      
}
